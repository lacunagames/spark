const calcPos = (centerX, centerY, x, y, deg, scale = 1) => {
  const distX = (x - centerX) * scale;
  const distY = (y - centerY) * scale;
  const rad = (-deg - 0) * (Math.PI / 180);
  const cosRad = Math.cos(rad);
  const sinRad = Math.sin(rad);

  return [
    centerX + distX * cosRad + distY * sinRad,
    centerY - distX * sinRad + distY * cosRad,
  ];
};

